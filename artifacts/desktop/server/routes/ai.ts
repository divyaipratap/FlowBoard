function normalizePlan(raw: unknown, prompt: string, requestedCount: number) {
  const data = raw && typeof raw === "object" ? raw as Record<string, unknown> : {};
  const sourceSteps = Array.isArray(data.nextSteps)
    ? data.nextSteps
    : Array.isArray(data.steps)
      ? data.steps
      : Array.isArray(data.tasks)
        ? data.tasks
        : [];

  let nextSteps = sourceSteps
    .filter((item): item is Record<string, unknown> => !!item && typeof item === "object")
    .map((item) => {
      const title = asString(item.title, asString(item.task, asString(item.name, "Review next project step")));
      const type = asString(item.type, inferType(`${title} ${asString(item.description)}`));
      const priority = asString(item.priority, inferPriority(`${title} ${asString(item.description)} ${asString(item.rationale)}`));
      const status = asString(item.status, "todo");

      return {
        title,
        description: asString(item.description, asString(item.details, asString(item.rationale, ""))),
        type: allowedTypes.has(type) ? type : "task",
        priority: allowedPriorities.has(priority) ? priority : "medium",
        status: allowedStatuses.has(status) ? status : "todo",
        rationale: asString(item.rationale, "Suggested by local AI"),
        acceptanceCriteria: asStringArray(item.acceptanceCriteria || item.acceptance_criteria || item.checklist),
        labels: asStringArray(item.labels).length ? asStringArray(item.labels) : ["ai-generated"],
        effort: asString(item.effort, "medium"),
      };
    });

  if (nextSteps.length === 0) {
    nextSteps = fallbackStepsFromPrompt(prompt, requestedCount);
  }

  if (nextSteps.length < requestedCount) {
    const existingTitles = new Set(nextSteps.map((step) => step.title.toLowerCase()));
    for (const step of supplementalSteps(prompt)) {
      if (nextSteps.length >= requestedCount) break;
      if (existingTitles.has(step.title.toLowerCase())) continue;
      nextSteps.push(step);
      existingTitles.add(step.title.toLowerCase());
    }
  }

  nextSteps = nextSteps
    .filter((step) => step.title.length > 0)
    .sort((a, b) => (priorityRank[b.priority] ?? 0) - (priorityRank[a.priority] ?? 0))
    .slice(0, Math.max(1, Math.min(requestedCount, 12)));

  return {
    summary: asString(data.summary, `Generated ${nextSteps.length} prioritized task ticket${nextSteps.length === 1 ? "" : "s"} from the prompt.`),
    blockers: Array.isArray(data.blockers) ? data.blockers.map((item) => String(item)) : [],
    strategy: asString(data.strategy, "Create the highest-priority tickets first, then refine from the board."),
    nextSteps,
  };
}

router.get("/ai/status", async (req, res) => {
  const endpoint = baseUrl(String(req.query.endpoint || ""));
  try {
    const response = await fetch(`${endpoint}/api/tags`, { signal: AbortSignal.timeout(5000) });
    if (!response.ok) {
      res.status(502).json({ ok: false, status: "unavailable", models: [] });
      return;
    }
    const data = await response.json() as { models?: Array<{ name: string; model?: string }> };
    res.json({ ok: true, status: data.models?.length ? "ready" : "no_models", models: data.models ?? [] });
  } catch {
    res.json({ ok: false, status: "unavailable", models: [] });
  }
});

router.post("/ai/project-plan", async (req, res) => {
  const { endpoint, model, project, issues, prompt, taskCount } = req.body ?? {};
  if (!model) {
    res.status(400).json({ error: "Model is required" });
    return;
  }
  const requestedCount = Number.isFinite(Number(taskCount)) ? Math.max(3, Math.min(Number(taskCount), 12)) : 6;

  const system = [
    "You are FlowBoard Task Intelligence, a local AI planner for solo builders.",
    "Return concise JSON only.",
    "Convert the user's prompt into multiple concrete task tickets.",
    `Create ${requestedCount} tickets unless the prompt clearly needs fewer.`,
    "Avoid duplicate tickets that already exist in the provided issue list.",
    "Every ticket must have title, description, type, priority, status, rationale, acceptanceCriteria, labels, and effort.",
    "Use priority critical only for blockers, data loss, security, crashes, or release-stopping work.",
    "Use high for important user-facing or prerequisite work, medium for normal execution, low for polish or optional work.",
    "Keep titles action-oriented and under 90 characters.",
    "Do not claim to change data; the user must review suggestions.",
  ].join(" ");

  const user = JSON.stringify({
    task: prompt || "Review this project and create a prioritized implementation plan.",
    ticketCount: requestedCount,
    project,
    issues,
    existingIssueTitles: Array.isArray(issues) ? issues.map((issue: { title?: string }) => issue.title).filter(Boolean).slice(0, 80) : [],
    responseShape: {
      summary: "short project assessment",
      strategy: "one sentence execution strategy",
      blockers: ["possible blocker"],
      nextSteps: [
        {
          title: "issue title",
          description: "why this matters and what to do",
          type: "task | bug | feature | story",
          priority: "low | medium | high | critical",
          status: "todo | in_progress | in_review | done",
          rationale: "short reason",
          acceptanceCriteria: ["testable done condition"],
          labels: ["short label"],
          effort: "low | medium | high",
        },
      ],
    },
  });

  try {
    const response = await fetch(`${baseUrl(endpoint)}/api/chat`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      signal: AbortSignal.timeout(60000),
      body: JSON.stringify({
        model,
        stream: false,
        format: "json",
        options: {
          temperature: 0.2,
          top_p: 0.9,
        },
        messages: [
          { role: "system", content: system },
          { role: "user", content: user },
        ],
      }),
    });

    if (!response.ok) {
      res.status(502).json({ error: "Ollama request failed" });
      return;
    }
    const data = await response.json() as { message?: { content?: string } };
    const content = data.message?.content || "{}";
    res.json(normalizePlan(parseLooseJson(content), prompt || "Review this project and suggest next steps.", requestedCount));
  } catch (error) {
    res.status(500).json({ error: "AI planning failed" });
  }
});

export default router;
