import { Router, type IRouter } from "express";

const router: IRouter = Router();
const DEFAULT_OLLAMA_URL = "http://localhost:11434";
const allowedTypes = new Set(["task", "bug", "feature", "story"]);
const allowedPriorities = new Set(["low", "medium", "high", "critical"]);
const allowedStatuses = new Set(["todo", "in_progress", "in_review", "done"]);
const priorityRank: Record<string, number> = { critical: 4, high: 3, medium: 2, low: 1 };

function baseUrl(raw?: string) {
  return (raw || DEFAULT_OLLAMA_URL).replace(/\/+$/, "");
}

function asString(value: unknown, fallback = "") {
  return typeof value === "string" && value.trim() ? value.trim() : fallback;
}

function asStringArray(value: unknown) {
  if (Array.isArray(value)) {
    return value.map((item) => String(item).trim()).filter(Boolean).slice(0, 6);
  }
  if (typeof value === "string" && value.trim()) {
    return value
      .split(/\n|;|\.\s+/)
      .map((item) => item.replace(/^[-*]\s*/, "").trim())
      .filter(Boolean)
      .slice(0, 6);
  }
  return [];
}

function parseLooseJson(content: string) {
  try {
    return JSON.parse(content);
  } catch {
    const match = content.match(/\{[\s\S]*\}/);
    if (!match) return {};
    try {
      return JSON.parse(match[0]);
    } catch {
      return {};
    }
  }
}

function inferPriority(text: string) {
  const value = text.toLowerCase();
  if (/\b(security|data loss|crash|broken|blocker|urgent|critical|release)\b/.test(value)) return "critical";
  if (/\b(high|important|risk|deadline|must|core|payment|auth)\b/.test(value)) return "high";
  if (/\b(cleanup|polish|nice|minor|later|optional)\b/.test(value)) return "low";
  return "medium";
}

function inferType(text: string) {
  const value = text.toLowerCase();
  if (/\b(bug|fix|error|crash|broken|fail|issue)\b/.test(value)) return "bug";
  if (/\b(user|workflow|journey|story)\b/.test(value)) return "story";
  if (/\b(feature|add|build|create|implement|support)\b/.test(value)) return "feature";
  return "task";
}

function fallbackStepsFromPrompt(prompt: string, requestedCount: number) {
  const fragments = prompt
    .split(/\n|;|,\s+(?=(?:add|build|create|fix|implement|make|support|design|test)\b)/i)
    .map((item) => item.replace(/^[-*]\s*/, "").trim())
    .filter((item) => item.length > 8)
    .slice(0, requestedCount);

  const source = fragments.length ? fragments : [
    "Clarify the goal and success criteria",
    "Break the work into implementation tasks",
    "Build the highest priority workflow first",
    "Verify behavior with a focused test pass",
  ];

  return source.slice(0, requestedCount).map((title) => ({
    title: title.length > 90 ? `${title.slice(0, 87)}...` : title,
    description: `Task generated from prompt: ${prompt}`,
    type: inferType(title),
    priority: inferPriority(title),
    status: "todo",
    rationale: "Generated by FlowBoard task intelligence from the user prompt.",
    acceptanceCriteria: ["The task is implemented", "The behavior is verified"],
    labels: ["ai-generated"],
    effort: "medium",
  }));
}

